    private String getDateString(Date date)
    {
        String time = "";
        int day = date.getDate();
        if (day < 10)
            time += "0";
        time += day;
        
        int month = date.getMonth();
        time += "/";
        
        if (month < 10)
            time += "0";
        time += month;
        
        int year = date.getYear() + 1900;
        time += "/" + year;

        return time;
    }
    
    
