    private void jDoctorSpecialtyComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDoctorSpecialtyComboActionPerformed
    
        String param = evt.paramString();
        
        //jDoctorPersonaliaEdit.setText(""+jDoctorSpecialtyCombo.getSelectedIndex());
        DefaultListModel listModel = (DefaultListModel)(jDoctorsList.getModel());
        listModel.removeAllElements();
        
        DoctorJpaController doctorController = controller.getDoctorJpaController();
        
        int doctorCount = doctorController.getDoctorCount();
        if (doctorCount == 0)
            return;
        
        String specialtyName = (String)jDoctorSpecialtyCombo.getSelectedItem();
        if (specialtyName == null)
            return;
        
        List<Doctor> doctors = doctorController.findDoctorEntitiesBySpecialty(specialtyName);
        
        int i = 0;
        for (Doctor d : doctors)
        {
            String doctorName = d.getFirstName() + " " + d.getLastName();
            listModel.add(i, doctorName);
            i++;
        }
        
        
    }//GEN-LAST:event_jDoctorSpecialtyComboActionPerformed

    private String dateToString(Date date)
    {
        return "" + date.getDate()+"/"+(date.getMonth()+1)+"/"+(date.getYear()+1900);    
    }
    
    private void jDoctorsListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jDoctorsListValueChanged
        
        String doctorName = jDoctorsList.getSelectedValue();
        if (doctorName == null)
            return;
        if (doctorName.length() == 0)
            return;
        
        String[] names = doctorName.split(" ");
        
        DoctorJpaController doctorController = controller.getDoctorJpaController();
        
        if (doctorController.getDoctorCount() == 0)
            return;
        
        Doctor d = doctorController.findDoctoryByFirstAndLastName(names[0], names[1]);
        jDoctorSpecialtyEdit.setText(d.getSpecialtyId().getSpecialtyName());
        jDoctorPersonaliaEdit.setText(d.getProfessionalTitle() + " " + d.getFirstName() + " " + d.getLastName());
        jDoctorEmploymentDateEdit.setText(dateToString(d.getEmployedSinceDate()));
        
        DefaultListModel patientsModel = (DefaultListModel)jPatientsByDoctorList.getModel();
        
        PatientJpaController patientController = controller.getPatientJpaController();
        if (patientController.getPatientCount() == 0)
            return;
        
        patientsModel.clear();
        List<Patient> patients = patientController.findPatientEntitiesByDoctorId(d.getDoctorId());
        int i = 0;
        
        for (Patient p : patients)
        {
            String patientName = p.getFirstName() + " " + p.getLastName();
            patientsModel.add(i, patientName);
            i++;
        }
    }//GEN-LAST:event_jDoctorsListValueChanged

    private void jRemovePatientFromDoctorButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRemovePatientFromDoctorButtonActionPerformed

        String patientName = jPatientsByDoctorList.getSelectedValue();
        if (patientName == null)
            return;
        if (patientName.length() == 0)
            return;
        
       String[] names = patientName.split(" ");
       
       PatientJpaController patientController = controller.getPatientJpaController();
       Patient p = patientController.findPatientByName(names[0], names[1]);
       if (p == null)
           return;
       try
       {
            patientController.destroy(p.getPatientId());
       }
       catch(Exception ioe){
           return;
       }
       
       int index = jPatientsByDoctorList.getSelectedIndex();
       DefaultListModel model = (DefaultListModel)jPatientsByDoctorList.getModel();
       model.removeElementAt(index);
       
       
    }//GEN-LAST:event_jRemovePatientFromDoctorButtonActionPerformed

